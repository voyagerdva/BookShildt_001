package e0019_Operations_time_measure;

public class MulAndDiv {
    static void multiByte(long max, byte b1, byte b2, long result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void multiShort(long max, short b1, short b2, long result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void multiInt(long max, int b1, int b2, long result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void multiLong(long max, long b1, long b2, long result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void multiFloat(long max, float b1, float b2, float result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void multiDouble(long max, double b1, double b2, double result) {
        for (long i = 0; i < max; i++) {
            result = b1 * b2;
        }
        System.out.println("\n    result = " + result);
    }

    // ======= DIVIDI =========================================================================================
    static void dividiByte(long max, byte b1, byte b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void dividiShort(long max, short b1, short b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void dividiInt(long max, int b1, int b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void dividiLong(long max, long b1, long b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void dividiFloat(long max, float b1, float b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }

    static void dividiDouble(long max, double b1, double b2, double result) {
        for (long i = 0; i < max; i++) {
            result = (double) b1 / b2;
        }
        System.out.println("\n    result = " + result);
    }
}
